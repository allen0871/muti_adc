				HAL_GPIO_WritePin(GPIOB,GPIO_PIN_9,GPIO_PIN_RESET);
			
				htim5.Instance->CCMR2 = 0x50;
				delay_us(100);
				htim5.Instance->CCR3 = htim5.Instance->ARR - 60;
				htim5.Instance->CCMR2 = 0x20;
				uint32_t t1,t2;
				t1 = refp;
				t2 = refn;
				tmp = nopn;
				printf("%d %d %d %d %d %d\n",t1, t2, tmp, rd1, rd2-rd3, rd3);
